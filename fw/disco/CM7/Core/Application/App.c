/* Step interval for the glow sweep: 10 ms/step -> ~1 s per 0->100 % ramp.  */
#define BL_GLOW_STEP_MS  10U

// * ************************************************************************ *
// *                                 TYPES                                    *
// * ************************************************************************ *

// * ************************************************************************ *
// *                             STATIC VARIABLES                             *
// * ************************************************************************ *

// * ************************************************************************ *
// *                           FORWARD DECLARATIONS                           *
// * ************************************************************************ *

// * ************************************************************************ *
// *                             GLOBAL FUNCTIONS                             *
// * ************************************************************************ *
void AppRun(void) {
  // DisplayInit();
  BacklightInit();

  for (;;) {
    /* Ramp up: 0 -> 100 % */
    for (uint8_t d = 0U; d <= BACKLIGHT_DUTY_MAX; d++) {
      BacklightSetDuty(d);
      // DisplayTask();
      HAL_Delay(BL_GLOW_STEP_MS);
    }
    /* Ramp down: 100 -> 0 % */
    for (uint8_t d = BACKLIGHT_DUTY_MAX; d-- > 0U; ) {
      BacklightSetDuty(d);
      // DisplayTask();
      HAL_Delay(BL_GLOW_STEP_MS);
    }
  }
}

